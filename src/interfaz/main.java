    public void leertxt() throws FileNotFoundException, IOException {
        BufferedReader br = new BufferedReader(new FileReader("palabras.txt"));
        BufferedReader br2 = new BufferedReader(new FileReader("sufijos.txt"));

        List<String> lineas = new ArrayList<String>();
        List<String> lineas2 = new ArrayList<String>();

        String linea = "";
        String linea2 = "";

        while ((linea = br.readLine()) != null) {
            lineas.add(linea);
        }
        while ((linea2 = br2.readLine()) != null) {
            lineas2.add(linea2);
        }

        br.close();
        br2.close();

        palabras = lineas.toArray(new String[]{});
        sufijos = lineas2.toArray(new String[]{});
    }

    public static String limpiarSignos(String texto) {
        String limpio = null;
        if (texto != null) {
            String valor = texto;
            valor = valor.toUpperCase();
            limpio = Normalizer.normalize(valor, Normalizer.Form.NFD);
            limpio = limpio.replaceAll("[^\\p{ASCII}(N\u0303)(n\u0303)(\u00A1)(\u00BF)(\u00B0)(U\u0308)(u\u0308)(\u002C)]", "");
            limpio = Normalizer.normalize(limpio, Normalizer.Form.NFC);
        }
        return limpio.toLowerCase();
    }
