    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed
        limpiarTabla();
        String textoOriginal = limpiarSignos(this.txtTexto.getText().trim().toLowerCase());
        textoOriginal = textoOriginal.replaceAll("[,|.|-|?|¡|!|/|;|:|¿|)|(|]", "");
        String[] text = textoOriginal.split(" ");
        try {
            leertxt();
        } catch (IOException ex) {
            Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (int i = 0; i < text.length; i++) {
            for (int j = 0; j < palabras.length; j++) {
                if (text[i].equals(palabras[j])) {
                    text[i] = "";
                }
            }
        }
        ArrayList<String> nuevoTexto = new ArrayList<>(Arrays.asList(text));
        for (int i = (nuevoTexto.size() - 1); i >= 0; i--) {
            if ("".equals(nuevoTexto.get(i))) {
                nuevoTexto.remove(i);
            }

        }

        String resultado = "";
        ArrayList<String> raices = new ArrayList<>();
        for (int i = 0; i < nuevoTexto.size(); i++) {
            resultado += nuevoTexto.get(i) + " ";
            for (int j = 0; j < sufijos.length; j++) {
                if (nuevoTexto.get(i).endsWith(sufijos[j]) && nuevoTexto.get(i).length() > (sufijos[j].length() + 2)) {
                    nuevoTexto.set(i, nuevoTexto.get(i).replace(sufijos[j], ""));
                    raices.add(nuevoTexto.get(i));
                }
            }
        }
        Set<String> quipu = new HashSet<String>(raices);
        for (String key : quipu) {
            modelo.addRow(new Object[]{key,Collections.frequency(raices, key)});
            this.tbRaices.setModel(modelo);

        }

        this.txtResultado.setText(resultado);
        System.out.println(raices);
    }//GEN-LAST:event_btnAnalizarActionPerformed

